-- ==============================================================================
-- レベル1: 条件抽出とソート（WHERE, ORDER BY）
-- 【ビジネス要求】2026年以降のアクティブ既存会員の中で、1回あたり3,000円以上購入した高単価リピーターを抽出
-- ==============================================================================

SELECT 
    user_id, 
    purchase_date, 
    revenue
FROM 
    sbux_sales_data
WHERE 
    purchase_date >= '2026-01-01' 
    AND revenue >= 3000
ORDER BY 
    revenue DESC;

